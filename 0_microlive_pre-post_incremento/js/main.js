let variable;



console.log('somma, postincremento');

variable = 10;
console.log(variable++);    //? 10
console.log(variable);      //? 11



console.log('somma, preincremento');

variable = 10;
console.log(++variable);    //? 11
console.log(variable);      //? 11






console.log('sottrazione, postdecremento');

variable = 10;
console.log(variable--);
console.log(variable);



console.log('sottrazione, predecremento');

variable = 10;
console.log(--variable);
console.log(variable);
